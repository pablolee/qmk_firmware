bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case JS_RCKT:
      if (record->event.pressed) {
        SEND_STRING("() => ");
      }
      return false;
      break;
   }
  return true;
}

void encoder_update_user(uint8_t index, bool clockwise) {
    if (index == 0) {
        if (clockwise) {
            tap_code(KC_VOLU);
        } else {
            tap_code(KC_VOLD);
        }
    }
    else if (index == 1) {
        if (clockwise) {
            tap_code(KC_PGDN);
        } else {
            tap_code(KC_PGUP);
        }
    }
}
